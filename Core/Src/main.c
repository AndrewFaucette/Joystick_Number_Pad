//Timer
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	// Check which timer triggered this callback
	if (htim == &htim16) // if the triggered timer was Timer 16
	{
		int ADC_RANGE = 4096;
		// Start ADC Conversions
		HAL_ADC_Start(&hadc1);
		HAL_ADC_Start(&hadc2);
		// Wait for ADC conversions to complete
		HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
		HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY);
		// Read ADC values
		uint16_t xjoyStepNumber = HAL_ADC_GetValue(&hadc1);
		uint16_t yjoyStepNumber = HAL_ADC_GetValue(&hadc2);
		// Convert ADC levels to a fraction of total
		float xjoyPercentage = ((float) xjoyStepNumber) / ADC_RANGE;
		float yjoyPercentage = ((float) yjoyStepNumber) / ADC_RANGE;

		// erase the cursor
		for (uint8_t y = cursor_y-1; y < cursor_y+2; y++) { // iterate over each row
		  for (uint8_t x = cursor_x-1; x < cursor_x+2; x++) { // iterate over each row
			  if(background[y][x] == 1) ST7735_DrawPixel(x, y, WHITE);
			  else ST7735_DrawPixel(x, y, BLACK);
		  }
		}
		// update the cursor position
		if (xjoyPercentage > .5) cursor_x += 5;
		else if (xjoyPercentage < .3) cursor_x -=5;
		if (cursor_x < 2 || cursor_x > 140) cursor_x = 1;
		else if (cursor_x > 126) cursor_x = 126;
		if (yjoyPercentage > .5) cursor_y += 5;
		else if (yjoyPercentage < .3) cursor_y -=5;
		if (cursor_y < 2 || cursor_y > 180) cursor_y = 1;
		else if (cursor_y > 158) cursor_y = 158;
		// draw the cursor
		for (uint8_t y = cursor_y-1; y < cursor_y+2; y++) { // iterate over each row
		  for (uint8_t x = cursor_x-1; x < cursor_x+2; x++) { // iterate over each row
			  ST7735_DrawPixel(x, y, BLUE);
		  }
		}
		time++;
	}
}
